# count()
# it gives you the your substring how many times come in your main string

# Original string
virat = 'Virat Kohli is the best player in the world and Virat Kohli is the best batsman in the planet'

# Substring to count
sub = 'Virat Kohli'

# Count the occurrences of the substring
count = virat.count(sub)

# Print the result
print(f"The substring '{sub}' appears {count} times in the string.")
